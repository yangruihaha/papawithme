// JavaScript Document
$(document).ready(function() {
    $('.login').bind("mouseover",function() {
		$(this).css("opacity","1");
        $('#login').css("opacity","1");
		
    }).bind("mouseout",function(){
		$(this).css("opacity","0.4");
        $('#login').css("opacity","0");
	})

});

