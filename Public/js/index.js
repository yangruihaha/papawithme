// JavaScript Document
$(document).ready(function() {
    $('.login').bind("mouseover",function() {
		$(this).fadeTo("fast",1);
        $('#login').fadeTo("fast",1);
		
    }).bind("mouseout",function(){
		$(this).fadeTo('fast',0.4);
        $('#login').fadeTo('fast',0);
	})

});

